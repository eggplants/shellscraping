#!/bin/bash
c=1
url=()
v6="|((([0-9a-f]{1,4}:){7}([0-9a-f]{1,4}|:))|(([0-9a-f]{1,4}:){6}(:[0-9a-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9a-f]{1,4}:){5}(((:[0-9a-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9a-f]{1,4}:){4}(((:[0-9a-f]{1,4}){1,3})|((:[0-9a-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9a-f]{1,4}:){3}(((:[0-9a-f]{1,4}){1,4})|((:[0-9a-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9a-f]{1,4}:){2}(((:[0-9a-f]{1,4}){1,5})|((:[0-9a-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9a-f]{1,4}:){1}(((:[0-9a-f]{1,4}){1,6})|((:[0-9a-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9a-f]{1,4}){1,7})|((:[0-9a-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$"

for i in "$@"; do
  if [[ "$i" =~ ^-v4$ ]]; then
    v6=""
  else
    url+=("$i")
  fi
done

for domain in ${url[*]}; do
  cat <<ANSWER
No. $c:
=============================
Name:
$domain
IP Address: 
$(
    res="$(nslookup "$domain" | tail -n +4 | tac)"
    if [[ $res =~ "t find" ]]; then
      echo "(Can't find)"
    else
      echo "$res" | grep -oE "[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$v6"
    fi
  )
Connectable?: 
$(
    if [[ -z "$(ping -w 1 "$domain")" ]]; then
      echo NO
    elif [[ "$(ping -w 1 "$domain")" =~ unreachable ]]; then
      echo NO
    else
      echo YES
      echo Traceroute:
      traceroute -w 1 -m 30 "$domain"
    fi
  )
=============================
ANSWER
  ((c++))
done 2>/dev/null
